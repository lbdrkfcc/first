            function fixNoScroll() {
                var dw = window.innerWidth;
                var cw = document.body.clientWidth;
                var pr = dw - cw;
                //$('body').css({'overflow':'hidden', 'padding-right': pr + 'px'});
                $('body').addClass('modal-open');
                $('body').css({
                    'padding-right': pr + 'px'
                });
                $('.header').css({
                    'padding-left': pr + 'px'
                    , 'margin-left': -pr + 'px'
                });
                $('.nav')({
                    'padding-right': pr-1 + 'px'
                });
            }
        });
