                $('.fi').toggleClass('fi_rev');
            });
            $('.js_navToggle').click(function () {
                var micon = $('.menu_icon').hasClass("icon-menu");
                if (micon == true) {
                    $('.menu_icon').removeClass('icon-menu');
                    $('.menu_icon').addClass('icon-close');
                    fixNoScroll();
                }
                else {
                    $('.menu_icon').removeClass('icon-close');
                    $('.menu_icon').addClass('icon-menu');
                    $('body').css({
                        'padding-right': 0
                    });
                    $('body').removeClass('modal-open')
                    $('.header').css({
                        'padding-left': 0
                        , 'margin-left': 0
                    , });
                    $('.nav')({
                        'padding-right': 0
                    });
                }
            });
            var h = $('.img_and_text span').height();
            $('.img_and_text>.text').css({
                'margin-top': -h / 2
            });
